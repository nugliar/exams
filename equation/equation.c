#include <stdio.h>

void	equation(int n)
{
	int a;
	int b;

	for (a = 0; a <= n && a < 100; a++)
	{
		b = n - a;
		if (b < 100 && a / 10 == b % 10)
			printf("A = %d, B = %d, C = %d\n", a / 10, a % 10, b / 10);
	}
}
