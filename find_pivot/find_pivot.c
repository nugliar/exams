int	find_pivot(int *arr, int n)
{
	int sum = 0;
	int cur = 0;
	
	for (int i = 0; i < n; i++)
		sum += arr[i];

	for (int i = 0; i < n; i++)
	{
		if (cur == sum - arr[i])
			return (i);
		cur += arr[i];
		sum -= arr[i];
	}
	return (-1);
}
