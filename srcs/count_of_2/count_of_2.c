int	count_of_2(int n)
{
	int i;
	int	num;
	int	count;

	if (n < 2)
		return (0);
	count = 0;
	i = 2;
	while (i <= n)
	{
		num = i;
		while (num)
		{
			if (num % 10 == 2)
				count++;
			num = num / 10;
		}
		i++;
	}
	return (count);
}
