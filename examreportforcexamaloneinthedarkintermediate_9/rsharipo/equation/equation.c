#include <stdio.h>

void	equation(int n)
{
	int			x;
	int			y;

	x = 0;
	while (x <= n)
	{
		y = n - x;
		if ((x < 100) && (y < 100) && (x / 10 == y % 10))
			printf("A = %d, B = %d, C = %d\n", x / 10, x % 10, y / 10);
		x++;
	}
}
