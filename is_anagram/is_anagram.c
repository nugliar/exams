int	is_anagram(char *a, char *b)
{
	static int	arr[128];
	int			i;

	if (!a || !b)
		return (0);
	i = 0;
	while (a[i])
		arr[(int)a[i++]] += 1;
	i = 0;
	while (b[i])
		arr[(int)b[i++]] -= 1;
	i = 0;
	while (i < 128)
		if (arr[i++] != 0)
			return (0);
	return (1);
}
