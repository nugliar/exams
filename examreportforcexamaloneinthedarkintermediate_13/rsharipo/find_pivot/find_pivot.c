int	find_pivot(int *arr, int n)
{
	int total = 0;
	int	cur = 0;

	for (int i = 0; i < n; i++)
		total += arr[i];
	for (int i = 0; i < n; i++)
	{
		if (cur == total - cur - arr[i])
			return (i);
		cur += arr[i];
	}
	return (-1);
}
